function ral_html (cols){
    var output = '<div class="ral clear">';
    var j = 0;
    for(var i in ralcolor) {
        if (!ralcolor.hasOwnProperty(i)) continue;
        if (j%cols === 0) {
            output += '<div class="col one_quarter first">';
        } else {
            output += '<div class="col one_quarter">';
        }
            output += '<div class="rcolor" style="background-color: '+ralcolor[i]+';"></div>';
            output += '<div class="rname">'+i+'<br/>'+ralname[i]+'</div>';
        output += '</div>';
        j++;
    }
    output = output+'</div>';
    return output;
}